// Função para gerar os cards do calendário
function gerarCalendario() {
  const container = document.getElementById('calendario-container');
  
  calendarioF1.forEach(corrida => {
    // Encontrar o horário correspondente
    const horario = horariosCorridas.find(h => h.round === corrida.round);
    
    // Criar o card da corrida
    const card = document.createElement('div');
    card.className = 'corrida-card';
    
    // Adicionar conteúdo ao card
    card.innerHTML = `
      <div class="corrida-header">
        <span class="round">ROUND ${corrida.round}</span>
        <span class="data">${corrida.data}</span>
      </div>
      <div class="corrida-content">
        <div class="corrida-info">
          <div class="pais">
            <img src="images/flags/${corrida.bandeira}" alt="Bandeira ${corrida.pais}" class="bandeira">
            <h3>${corrida.nome}</h3>
          </div>
          <div class="circuito">
            <p>${corrida.circuito}</p>
            <p class="cidade">${corrida.cidade}, ${corrida.pais}</p>
          </div>
          <div class="horario">
            <p class="horario-titulo">Horário da Corrida (Brasília)</p>
            <p class="horario-valor">${horario ? horario.horario_brasilia : 'A confirmar'}</p>
            <p class="horario-local">Horário Local: ${horario ? horario.horario_local : 'A confirmar'}</p>
          </div>
        </div>
        <div class="circuito-mapa">
          <img src="images/circuits/${corrida.minimapa}" alt="Circuito ${corrida.circuito}" class="minimapa">
        </div>
      </div>
    `;
    
    // Adicionar o card ao container
    container.appendChild(card);
  });
}

// Inicializar o calendário quando a página carregar
document.addEventListener('DOMContentLoaded', gerarCalendario);
